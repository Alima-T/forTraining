package lessons.lesson10;

public class Book {
    private String name;
    private String author;
    private int page;
    private int price;

    public String getName() {
        return name;
    }
    public String getAuthor() {
        return author;
    }
    public int getPage(){
        return page;
    }
    public int getPrice() {
        return price;
    }
}
